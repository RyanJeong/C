#include <stdio.h>              /*  표준 입출력 라이브러리의 함수를 이용    */

int main(void)                  /*  함수 main을 정의, 매개변수는 없다.  */
{                               /*  main 함수 시작  */
    printf("Hello, World\n");   /*  문자출력을 위해 printf 함수를 호출  */

    return 0;                   /*  main 함수 종료  */
}
